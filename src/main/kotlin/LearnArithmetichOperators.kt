fun main(){

    //Não soma
    var mousetest = 325
    mousetest + 1

    println(mousetest)

    //A variável devce receber ela somada a um valor
    var mouse = 325

    mouse = mouse + 3
    println(mouse)
    mouse = mouse - 3
    println(mouse)
    mouse = mouse * 5
    println(mouse)
    mouse = mouse / 5
    println(mouse)

    //Simplificando
    mouse += 3
    println(mouse)
    mouse -= 3
    println(mouse)
    mouse *= 5
    println(mouse)
    mouse /= 5
    println(mouse)

    //Operação Resto %
    var mousefloat: Float = 10f
    mousefloat /= 3

    println(mousefloat)

    mouse = 50 % 15
    println(mouse)

}